"""
Program: Check Even or Odd

This program checks whether a given number
is even or odd.
"""

# --------------------------------------------------
# INPUT
# --------------------------------------------------

number = int(input("Enter a number: "))

# --------------------------------------------------
# LOGIC
# --------------------------------------------------

if number % 2 == 0:
    print(number, "is Even")
else:
    print(number, "is Odd")

# --------------------------------------------------
# END OF PROGRAM
# --------------------------------------------------
